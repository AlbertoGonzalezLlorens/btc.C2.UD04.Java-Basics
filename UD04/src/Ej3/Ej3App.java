package Ej3;

public class Ej3App {

	public static void main(String[] args) {
		int x = 5;
		int y = 10;
		double n = 5.5;
		double m = 10.5;
		
		System.out.println("Valor x= " + x);
		System.out.println("Valor y= " + y);
		System.out.println("Valor n= " + n);
		System.out.println("Valor m= " + m);
		System.out.println("x + y= " + (x+y));
		System.out.println("x - y= " + (x-y));
		System.out.println("x * y= " + (x*y));
		System.out.println("x / y= " + (x/y));
		System.out.println("x % y= " + (x%y));
		System.out.println("n + m= " + (n+m));
		System.out.println("n - m= " + (n-m));
		System.out.println("n * m= " + (n*m));
		System.out.println("n / m= " + (n/m));
		System.out.println("n % m= " + (n%m));
		System.out.println("x + n= " + (x+n));
		System.out.println("y / m= " + (y/m));
		System.out.println("y % m= " + (y%m));
		System.out.println("Valor 2*x= " + (2*x));
		System.out.println("Valor 2*y= " + (2*y));
		System.out.println("Valor 2*n= " + (2*n));
		System.out.println("Valor 2*m= " + (2*m));
		System.out.println("Suma total= " + (x+y+n+m));
		System.out.println("Producto total= " + (x*y*n*m));

	}

}
